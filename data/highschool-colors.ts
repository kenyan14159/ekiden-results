// ハッシュ関数を使って学校名から一意の色を生成
function hashStringToColor(str: string): string {
  let hash = 0
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash)
  }
  
  // HSLを使って視覚的に区別しやすい色を生成
  const hue = Math.abs(hash % 360)
  const saturation = 65 + (Math.abs(hash) % 20) // 65-85%
  const lightness = 40 + (Math.abs(hash >> 8) % 15) // 40-55%
  
  return hslToHex(hue, saturation, lightness)
}

// HSLをHEXに変換
function hslToHex(h: number, s: number, l: number): string {
  l /= 100
  const a = s * Math.min(l, 1 - l) / 100
  const f = (n: number) => {
    const k = (n + h / 30) % 12
    const color = l - a * Math.max(Math.min(k - 3, 9 - k, 1), -1)
    return Math.round(255 * color).toString(16).padStart(2, '0')
  }
  return `#${f(0)}${f(8)}${f(4)}`
}

export function getHighSchoolColor(schoolName: string): string {
  // 直接定義されている色を優先
  if (highSchoolColors[schoolName]) {
    return highSchoolColors[schoolName]
  }
  
  // 定義されていない学校はハッシュから色を生成
  return hashStringToColor(schoolName)
}

